package Entity;

/**
 *
 * @author USER
 */
public class Showtime {
    private String id, time; 

    public Showtime() {
    }

    public Showtime(String id, String time) {
        this.id = id;
        this.time = time;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    @Override
    public String toString() {
        return "Showtime{" + "id=" + id + ", time=" + time + '}';
    }
    
    
}
